#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	int	i;

	if (nb == -2147483648)
	{
		ft_putnbr(nb / 10);
		ft_putchar('8');
		return ;
	}
	if (nb < 0)
	{
		ft_putchar('-');
		ft_putnbr(-nb);
		return ;
	}
	if (nb > 9)
	{
		i = nb % 10;
		ft_putnbr(nb / 10);
		ft_putchar(i + 48);
	}
	if (nb < 10)
	{
		ft_putchar(nb + 48);
		return ;
	}
}
